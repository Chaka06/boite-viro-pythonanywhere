        // Headers
        to_email: donnees.beneficiaire_email,
        to_name: donnees.beneficiaire_nom,
        from_name: 'BOITE-VIRO',
        langue: lang,
        
        // Traductions
        subject: translations.subject_initiation + ' - ' + donnees.banque,
        title: translations.title_initiation,
        dept_operations: translations.dept_operations,
        securite_msg: translations.securite_msg,
        titre_principal: translations.titre_principal_initiation,
        reference_label: translations.reference_label,
        salutation: translations.salutation,
        message_principal: translations.message_principal_initiation,
        montant_label: translations.montant_label,
        details_operation: translations.details_operation,
        beneficiaire_label: translations.beneficiaire_label,
        donneur_ordre_label: translations.donneur_ordre_label,
        banque_label: translations.banque_label,
        date_label: translations.date_label,
        statut_label: translations.statut_label,
        statut_traitement: translations.statut_traitement,
        info_importante: translations.info_importante,
        message_info: translations.message_info,
        temps_traitement_label: translations.temps_traitement_label,
        temps_traitement: translations.temps_traitement,
        message_automatique: translations.message_automatique,
        droits_reserves: translations.droits_reserves,
        footer_msg: translations.footer_msg,
        systeme_gestion: translations.systeme_gestion,
        
        // Données du virement
        beneficiaire_nom: donnees.beneficiaire_nom,
        donneur_ordre: donnees.donneur_ordre,
        montant: donnees.montant,
        devise: donnees.devise,
        banque: donnees.banque,
        banque_emettrice: donnees.banque,
        date: donnees.date,
        numero_virement: donnees.numero_virement
